//==============================================================================

runtime_windows::runtime_windows(data const & data) noexcept
    : runtime_base(data)
{
}

//==============================================================================

void runtime_windows::handle_messages(void) noexcept
{
    MSG msg;
    memory::zero(&msg);

    if (PeekMessage(&msg, 0, 0, 0, PM_REMOVE))
    {
        TranslateMessage(&msg);
        DispatchMessage(&msg);
    }
}

